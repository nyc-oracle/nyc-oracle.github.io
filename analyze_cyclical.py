def fetch_data(url, desc=""):
    """Fetch data from Socrata API without auth token."""
    print(f"  Fetching: {desc or url[:80]}")
    try:
        resp = requests.get(url, timeout=60)
        resp.raise_for_status()
        data = resp.json()
        print(f"    -> Got {len(data)} rows")
        return data
    except Exception as e:
        print(f"    -> ERROR: {e}")
        return None

def compute_stl_strength(series_values, period=12):
    """
    Compute STL seasonal strength.
    Returns strength in [0, 1] where 1 = perfectly seasonal.
    """
    if len(series_values) < period * 2:
        return None
    s = pd.Series(series_values, dtype=float)
    s = s.dropna()
    if len(s) < period * 2:
        return None
    try:
        stl = STL(s, period=period, robust=True)
        res = stl.fit()
        var_resid = np.var(res.resid)
        var_seas_resid = np.var(res.seasonal + res.resid)
        if var_seas_resid == 0:
            return 0.0
        strength = max(0, 1 - var_resid / var_seas_resid)
        return round(strength, 4)
    except Exception as e:
        print(f"    STL error: {e}")
        return None

def peak_month_index(series_values, period=12, date_index=None):
    """
    Find the peak calendar month (1-12) of the STL seasonal component.
    date_index: optional DatetimeIndex aligned with series_values — used to
    group seasonal component by actual calendar month rather than series position.
    """
    if len(series_values) < period * 2:
        return None
    s = pd.Series(series_values, dtype=float)
    s = s.dropna()
    if len(s) < period * 2:
        return None
    try:
        stl = STL(s, period=period, robust=True)
        res = stl.fit()
        if date_index is not None and len(date_index) == len(res.seasonal):
            # Use .values to avoid pandas label-alignment (res.seasonal has RangeIndex)
            seas_with_dates = pd.Series(res.seasonal.values, index=date_index)
            monthly_avgs = seas_with_dates.groupby(seas_with_dates.index.month).mean()
            return int(monthly_avgs.idxmax())
        else:
            # Fallback: positional grouping (only accurate if series starts in Jan)
            seas = pd.Series(res.seasonal)
            month_avg = [seas[i::12].mean() for i in range(12)]
            return month_avg.index(max(month_avg)) + 1
    except:
        return None

MONTH_NAMES = {1:'Jan',2:'Feb',3:'Mar',4:'Apr',5:'May',6:'Jun',
               7:'Jul',8:'Aug',9:'Sep',10:'Oct',11:'Nov',12:'Dec'}

def month_label(m):
    if m is None:
        return "N/A"
    return MONTH_NAMES.get(m, str(m))

def assign_abundance(daily_counts):
    """
    Classify daily counts into five ordinal abundance levels using quintile breaks.

    This is the categorization used in prophecy-source-data/*_abundance.csv. Thresholds
    are computed from the full-year distribution of the specific dataset being classified,
    so labels are relative within each dataset (not cross-dataset).

    Bins (quintiles of the input series):
        Very Low  : 0th–20th percentile
        Low       : 20th–40th percentile
        Medium    : 40th–60th percentile
        High      : 60th–80th percentile
        Very High : 80th–100th percentile

    Ties at a boundary are assigned to the lower bin. Example for dog bites 2024 (n=366):
        Very Low  <= 8   (P20=8;  n=101 including ties)
        Low        9–10  (P40=10; n=73)
        Medium    11–12  (P60=12; n=67)
        High      13–14  (P80=14; n=56)
        Very High >= 15  (n=69)

    Equivalent pandas one-liner:
        pd.qcut(series, q=5, labels=['Very Low','Low','Medium','High','Very High'])
    """
    vals = list(daily_counts)
    n = len(vals)
    s = sorted(vals)
    t = [s[int(n * p / 100)] for p in (20, 40, 60, 80)]  # P20, P40, P60, P80
    bins = ["Very Low", "Low", "Medium", "High", "Very High"]
    return [
        bins[0] if v <= t[0] else
        bins[1] if v <= t[1] else
        bins[2] if v <= t[2] else
        bins[3] if v <= t[3] else
        bins[4]
        for v in vals
    ]


def season_desc(month_num):
    if month_num is None:
        return "N/A"
    if month_num in [12, 1, 2]:
        return "peaks in winter"
    elif month_num in [3, 4, 5]:
        return "peaks in spring"
    elif month_num in [6, 7, 8]:
        return "peaks in summer"
    elif month_num in [9, 10, 11]:
        return "peaks in fall"
    return "N/A"

