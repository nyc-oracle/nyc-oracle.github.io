raw_agency = fetch_data(url_311_agency, "311 monthly by agency")

agency_results = []
if raw_agency:
    df_agency = pd.DataFrame(raw_agency)
    df_agency['n'] = pd.to_numeric(df_agency['n'], errors='coerce')
    df_agency['month'] = pd.to_datetime(df_agency['month'], errors='coerce')
    df_agency = df_agency.dropna(subset=['month', 'n'])
    df_agency = df_agency.sort_values('month')

    agencies = df_agency['agency'].unique()
    print(f"  Found {len(agencies)} agencies")

    for ag in agencies:
        sub = df_agency[df_agency['agency'] == ag].copy()
        sub = sub.set_index('month').sort_index()
        full_idx = pd.date_range(sub.index.min(), sub.index.max(), freq='MS')
        sub = sub.reindex(full_idx)['n']

        n_months = sub.notna().sum()
        sparsity = sub.isna().sum() / len(sub)

        if n_months >= 36 and sparsity < 0.3:
            filled = sub.fillna(sub.median())
            vals = filled.values
            strength = compute_stl_strength(vals, period=12)
            pk = peak_month_index(vals, period=12, date_index=filled.index)
            if strength is not None:
                agency_results.append({
                    'agency': ag,
                    'n_months': n_months,
                    'total_requests': int(sub.sum()),
                    'seasonal_strength': strength,
                    'peak_month': month_label(pk),
                    'pattern': season_desc(pk)
                })

    agency_results = sorted(agency_results, key=lambda x: x['seasonal_strength'], reverse=True)
    print(f"  Scored {len(agency_results)} agencies")

